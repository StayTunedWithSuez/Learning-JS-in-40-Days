// Creating a Map with key-value pairs
const entries = new Map([
  ["name", "Suez"],
  ["age", 23]
]);

// Converting the Map into an object using Object.fromEntries()
const objEntries = Object.fromEntries(entries);

// Displaying the resulting object
console.log(objEntries); // Output: { name: "Suez", age: 23 }








